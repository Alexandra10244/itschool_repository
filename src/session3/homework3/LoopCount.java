package session3.homework3;

public class LoopCount {

    public static void main(String[] args) {

        for (int number = 1; number <= 10; number++) {
            System.out.print(number + " ");
        }
        System.out.println();
        for (int number = 10; number >= 1; number--) {
            System.out.print(number + " ");
        }

    }
}

